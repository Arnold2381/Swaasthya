exports.putOutcome = async (req, res) => {
  const { symptoms, outcome, predictions, disease } = req.body;

  const test = new Test({
    symptoms
  });

  if(disease) {
    test.disease = disease;
  }

  if(predictions) {
    test.predictions = predictions;
  }

  if(outcome) {
    test.outcome = outcome;
  }

  await test.save();

  res.json({
    message: "Outcome stored"      
  });
}