exports.predictFromSymptoms = async (req,res) => {
  const { symptoms } = req.body;
  const response = await axios.post('https://medtech-ml.herokuapp.com/predict', {
    symptoms
  });
  
  res.json({
      message: "Predicted disease from symptoms",
      data: {
        predictions: response.data,
      }
  });
};

